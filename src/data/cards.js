// ROUND-ROBIN CATEGORY SYSTEM: Get one card from each category for balanced gameplay
export const getRoundOfCards = (excludeIds = [], excludeTypes = []) => {
  // Updated to 5 categories (removed 'style')
  const categories = ['creature', 'environment', 'power', 'attribute', 'mood'];
  const round = [];
  
  // Power level targets for balanced gameplay (mix of low, medium, high power)
  const powerTargets = [
    { min: 1, max: 7 },   // Low power
    { min: 8, max: 12 },  // Medium power  
    { min: 13, max: 20 }  // High power
  ];
  
  for (const category of categories) {
    // Skip if this category type is excluded
    if (excludeTypes.includes(category)) {
      continue;
    }
    
    // Get available cards for this category
    const categoryCards = cardData.filter(card => 
      card.type === category && 
      !excludeIds.includes(card.id)
    );
    
    if (categoryCards.length > 0) {
      // Try to get a balanced power level (33% chance for each power range)
      const powerRange = powerTargets[Math.floor(Math.random() * powerTargets.length)];
      
      // First try to get a card in the selected power range
      let targetCards = categoryCards.filter(card => 
        card.power >= powerRange.min && card.power <= powerRange.max
      );
      
      // If no cards in target range, use any available card
      if (targetCards.length === 0) {
        targetCards = categoryCards;
      }
      
      // Randomly select one card from the target cards
      const randomIndex = Math.floor(Math.random() * targetCards.length);
      round.push(targetCards[randomIndex]);
    }
  }
  
  // Shuffle the round so categories don't always appear in the same order
  return round.sort(() => 0.5 - Math.random());
};

// LEGACY SUPPORT: Keep the old function for backward compatibility
export const getRandomCards = (count = 5, excludeIds = [], excludeTypes = []) => {
  // For round-robin system, ignore count and use getRoundOfCards
  return getRoundOfCards(excludeIds, excludeTypes);
};

// Calculate total power from selected cards
export const calculateTotalPower = (cards) => {
  return cards.reduce((total, card) => total + card.power, 0);
};

// Get available card categories for debugging
export const getCardCategories = () => {
  const categories = {};
  cardData.forEach(card => {
    if (!categories[card.type]) {
      categories[card.type] = [];
    }
    categories[card.type].push(card);
  });
  return categories;
};

